# input a float value for variable a here
a = float( input("Enter the first number: ") )
# input a float value for variable b here
b = float( input("Enter the second number: ") )
# output the result of addition here
addition = a + b
print("The result of adding", a, "and", b, "is:", addition)
# output the result of subtraction here
subtraction = a - b
print("The result of subtracting", b, "from", a, "is:", subtraction)
# output the result of multiplication here
multiplication = a * b
print("The result of multiplying", a, "by", b, "is:", multiplication)
# output the result of division here
division = a / b
print("The result of dividing", a, "by", b, "is:", division)

